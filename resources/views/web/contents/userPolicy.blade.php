            <p style="font-weight:900;">一、如何成为会员？</p>        
            <p>
              1.消费特定产品并通过手机扫描读取特定产品附贴的喜豆二维码，依提示完成APP软件下载、注册程序即可成为喜豆会员。<br/>
              2.本规则所称特定产品，指附贴有喜豆二维码的消费品，每个喜豆二维码成功扫描读取后不接受再次扫描读取。<br/>
              3.注册会员过程中，喜豆需要收集到能够识别会员身份的相关个人信息，并采取有效措施对个人信息予以保护
            </p>
            <p style="color:red;">3.注册会员过程中，喜豆需要收集到相关个人信息，识别会员身份，并采取有效措施对个人信息予以保护。</p>          
            <p style="font-weight:900;">二、会员享有什么权益？</p>
            <p>
              4.会员可以获取喜豆消费有奖竞猜中奖的机会。会员扫描读取特定产品喜豆二维码后，通过点击打开喜豆消费有奖竞猜区，竞猜内容由红色球号码区和蓝色球号码区组成，其中红色球号码序号为1--33，蓝色球号码序号为1--16，会员任意选择6个红色球号码和1个蓝色球号码，或者默认由喜豆系统随机自动选取，点击上传，最高奖项金额以5万元为上限，具体详情见《喜豆会员消费有奖竞猜规则》。<br/> 
              5.会员可以分享喜豆消费资讯信息。喜豆不定期向会员推送各类消费资讯信息，该类消费资讯信息可以是视频，也可以是图片或文字，会员可以选择阅读，<p style="color:red;">但不得用于不正当用途及侵犯他人合法权益</p>。除法律法规明确规定外，会员应自行对依喜豆消费资讯信息进行的交易负责。<br/> 
              6.会员可以获取喜豆在线商务平台服务。喜豆构建出售商品及购买商品的移动交易平台，会员可以选择依喜豆在线商务服务规则入场交易。<br/> 
              7.会员可以获取消费积分并享有相应权益。会员每次消费满一元即可累计一个积分，单次消费不足一元的金额，不计入积分，会员累计积分可用于换购喜豆在线商务平台商品，也可以依《喜豆会员消费有奖竞猜规则》参加消费有奖竞猜活动，但会员使用积分进行的消费不再累计积分。
            </p>
            <p style="font-weight:900;">三、会员承担什么义务？</p>
            <p>
              8.会员应当遵守法律、法规及本规则，遵守《喜豆会员消费有奖竞猜规则》，并有效维持喜豆软件的可使用状态及安全，自行承担因使用喜豆软件所发生的数据流量费用等。<br/>
              9.如获奖会员为自然人，依法承担缴纳个人所得税的义务。<br/>
              10.会员理解并同意，喜豆保留单方面对应用软件的全部或部分服务内容变更、暂停、终止的权利，但喜豆应当采取合理措施保护会员的权益。
            </p>
            <p style="font-weight:900;">四、发生争议如何解决？</p>
            <p>
              11.特定产品的产品质量责任由生产商或销售商负责，附贴喜豆二维码并不表明喜豆对其产品质量作出任何保证性的承诺。<br/>
